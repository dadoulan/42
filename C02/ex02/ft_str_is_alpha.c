int		ft_str_is_alpha(char *str)
{
	int i;
	int cmp;

	i = 0;
	cmp = 0;
	while (str[i] != '\0')
	{
		if ((str[i] < 'A') || ('Z' < str[i] && str[i] < 'a')
			|| ('z' < str[i]))
			cmp++;
		i++;
	}
	if (cmp > 0)
		return (0);
	else
		return (1);
}
