int		ft_str_is_numeric(char *str)
{
	int i;
	int cmp;

	i = 0;
	cmp = 0;
	while (str[i] != '\0')
	{
		if (str[i] < '0' || '9' < str[i])
			cmp++;
		i++;
	}
	if (cmp > 0)
		return (0);
	else
		return (1);
}
