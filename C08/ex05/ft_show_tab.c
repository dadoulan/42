void ft_show_tab(struct s_stock_str *par)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (par[i])
	{
		write (1, &par[j++].str, 1);
		write (1, "\n", 1);
		write (1, &par[j++].size, 1);
		write (1, "\n", 1);
		write (1, &par[j++].copy, 1);
		write (1, "\n", 1);
		i++;
		j = 0;
	}
}
